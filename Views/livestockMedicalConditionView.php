            <h2 class="mb-3 mt-5">#1. About</h2>
            <p>The disease occurs in India in both generalized and localized forms, udder, inner thigh, lips and nostrils. The disease is of zoonotic importance manifesting lesions on the hands and fingers of milkers. The methods of treatment and prevention are similar to those recommended for cow-pox. Since buffaloes wallow in marshy places care should be taken to see that the wounds are cleaned well and kept free form files. Attempts to develop a vaccine against buffalo-pox have not given encouraging results.
            </p>
        
            <h2 class="mb-3 mt-5">#3. Prevention &amp; Control</h2>
            <p>The lessons heal by themselves in the normal course and the adoption of special measures is not called for; only the usual rules of hygiene need to be observed. The lesions should be cleaned with a 1:1,000 solution of potassium permanganate followed by the application of an antiseptic ointment such as 1:110 boric acids. The affected animals should be isolated and milked by separate milkers. Milk from affected animals should be boiled before use.</p>
